# MAGIC ここまで来れたこと、おめでとうございます！このコースは、pythonとデータ分析で可能なことの上っ面をなぞっただけに過ぎません。旅を続けるため、以下の資料をご覧ください。[また、Databricksの他のコースもご覧いただけます。](https://academy.databricks.com)
# MAGIC 
# MAGIC Pythonをさらに学習するために：
# MAGIC 
# MAGIC - [Introduction to Computational Thinking and Data Science](https://ocw.mit.edu/courses/6-0002-introduction-to-computational-thinking-and-data-science-fall-2016/)は、MITのプログラミング入門クラスです。[クラスのためのJohn Guttag氏の著書もあります。](https://www.amazon.com/Introduction-Computation-Programming-Using-Python/dp/0262529629/ref=sr_1_1?crid=36AMZ24HWKRMQ1)どちらもPythonの優れた入門資料です
# MAGIC - [Leet Code](https://leetcode.com/)は、コーディング問題の練習に最適なリソースです
# MAGIC - [Fluent Python](https://www.oreilly.com/library/view/fluent-python/9781491946237/) by Luciano Ramalhoは、初心者に適したPythonの良い入門書です
# MAGIC 
# MAGIC データ分析、データサイエンスをさらに学習するために：
# MAGIC 
# MAGIC - [Python for Data Analysis](https://www.oreilly.com/library/view/python-for-data/9781449323592/)は、`pandas`の開発者であるWes McKinneyによって書かれました。科学的なプログラミングの理解を深めることができます
# MAGIC - [fast.ai](https://www.fast.ai/)は、ニューラルネットワークを紹介する業界標準の無料クラスを提供します
# MAGIC - [Deep Learning for Coders with fastai and PyTorch](https://www.oreilly.com/library/view/deep-learning-for/9781492045519/)は、`Fastai`の開発者であるJeremy HowardとSylvain Guggerによって書かれています
# MAGIC - [Kaggle](https://www.kaggle.com/)は、データサイエンティストのためのオンラインコンペティションサイトです。高ランクのプレイヤーは、就職の心配をする必要がありません :-)
# MAGIC - [Deep Learning with Python](https://www.oreilly.com/library/view/deep-learning-with/9781617296864/)は、ニューラルネットワークライブラリ`tensorflow`の `keras`APIの最初の開発者であるFrancois Cholletによって書かれました

